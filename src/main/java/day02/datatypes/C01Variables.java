package day02.datatypes;

public class C01Variables {

    public static void main(String[] args) {

        byte stdAge = 25;
        System.out.println(stdAge);


        short numOfStInHighSchool = 3245;
        System.out.println(numOfStInHighSchool);


        int populationOfMiami=1200000;
        System.out.println(populationOfMiami);


        // if u create long valuable, use "L" at the end of the value when the value is out of "int range"

        long populationOfTheWorld = 70000000000L;
        System.out.println(populationOfTheWorld);


        float shirtPrice = 23.99F;
        System.out.println(shirtPrice);


        double weightOfACell = 0.00000000012;
        System.out.println(weightOfACell);


        boolean isRetired = false;
        System.out.println( isRetired);



        char initial = 'A';
        System.out.println(initial);



        String stdName = "Mark_Twain";
        System.out.println(stdName);



    }
}
